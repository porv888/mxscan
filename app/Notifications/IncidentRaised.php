<?php

namespace App\Notifications;

use App\Models\Incident;
use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;
use Illuminate\Notifications\Messages\SlackMessage;
use Illuminate\Notifications\Notification;

class IncidentRaised extends Notification implements ShouldQueue
{
    use Queueable;

    public function __construct(public Incident $incident)
    {
        //
    }

    /**
     * Get the notification's delivery channels.
     */
    public function via($notifiable): array
    {
        $channels = [];
        $prefs = $notifiable->getNotificationPrefs();

        if ($prefs->email_enabled) {
            $channels[] = 'mail';
        }

        if ($prefs->isSlackConfigured()) {
            $channels[] = 'slack';
        }

        return $channels;
    }

    /**
     * Get the mail representation of the notification.
     */
    public function toMail($notifiable): MailMessage
    {
        $domain = $this->incident->domain;
        $severityEmoji = $this->getSeverityEmoji();
        
        return (new MailMessage)
            ->subject("MXScan Alert: {$this->incident->kind} - {$domain->domain}")
            ->greeting("Security Alert {$severityEmoji}")
            ->line("An incident has been detected for your domain **{$domain->domain}**:")
            ->line("**Issue:** {$this->incident->message}")
            ->line("**Severity:** " . ucfirst($this->incident->severity))
            ->line("**Detected:** {$this->incident->created_at->format('M j, Y \a\t g:i A')}")
            ->action('View Domain Details', url("/domains/{$domain->id}"))
            ->line('This alert was generated by MXScan\'s monitoring system.')
            ->salutation('Best regards, The MXScan Team');
    }

    /**
     * Get the Slack representation of the notification.
     */
    public function toSlack($notifiable): SlackMessage
    {
        $domain = $this->incident->domain;
        $severityEmoji = $this->getSeverityEmoji();
        $color = $this->getSeverityColor();

        return (new SlackMessage)
            ->from('MXScan', ':shield:')
            ->to('#alerts')
            ->attachment(function ($attachment) use ($domain, $severityEmoji, $color) {
                $attachment->title("MXScan Alert: {$this->incident->kind}", url("/domains/{$domain->id}"))
                    ->content("{$severityEmoji} **{$domain->domain}**: {$this->incident->message}")
                    ->color($color)
                    ->fields([
                        'Severity' => ucfirst($this->incident->severity),
                        'Domain' => $domain->domain,
                        'Time' => $this->incident->created_at->format('M j, Y \a\t g:i A'),
                    ]);
            });
    }

    /**
     * Get the array representation of the notification.
     */
    public function toArray($notifiable): array
    {
        return [
            'incident_id' => $this->incident->id,
            'domain_id' => $this->incident->domain_id,
            'domain' => $this->incident->domain->domain,
            'kind' => $this->incident->kind,
            'severity' => $this->incident->severity,
            'message' => $this->incident->message,
        ];
    }

    /**
     * Get emoji for severity level
     */
    protected function getSeverityEmoji(): string
    {
        return match ($this->incident->severity) {
            'critical' => 'ðŸš¨',
            'warning' => 'âš ï¸',
            'info' => 'â„¹ï¸',
            default => 'ðŸ“‹'
        };
    }

    /**
     * Get color for Slack attachment
     */
    protected function getSeverityColor(): string
    {
        return match ($this->incident->severity) {
            'critical' => 'danger',
            'warning' => 'warning',
            'info' => 'good',
            default => '#cccccc'
        };
    }
}
